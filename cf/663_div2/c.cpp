int n;
const int MOD = 1e9 + 7;
int bin_p(int a, int b){
    int ans = 1;
    while (b > 0){
        if (b & 1)
            ans = (ans * a) % MOD;
        a = (a * a) % MOD;
        b >>= 1;
    }
    return ans;
}
int solve() {
    if (!(cin >> n))
        return 1;
    int ans = 1;
    for (int i = 1; i <= n; i++)
        ans = (ans * i) % MOD;
    cout << (ans + MOD - (bin_p(2, n - 1))) % MOD << '\n';
    return 0;
}
 
