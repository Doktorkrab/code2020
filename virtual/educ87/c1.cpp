int n;
double pi = acos(-1.L);
 
int solve() {
    if (!(cin >> n))
        return 1;
    n *= 2;
    double angle = (n - 2.) / n * pi;
    cout << fixed << setprecision(20) << tan(angle / 2) << '\n';
    return 0;
}
 
