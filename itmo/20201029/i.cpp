int n, k;
const ll MOD = 998244353;

ll rec(ll k){
    if (k == 0)
        return 1;
    return ((n / 2) * rec(k - 1) + ((n - 1) / 2)) % MOD;
}
 
int solve() {
    if (!(cin >> n >> k))
        return 1;

    cout << rec(k) << '\n';
    return 0;
}
 
signed main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int tests = 1e9;
//    cin >> tests;
    for (int i = 1; i <= tests; i++) {
        if (solve())
            break;
#ifdef LOCAL
        cout << "------------------------------\n";
#endif
    }
}
