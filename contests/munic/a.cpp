#define TMAX(type) numeric_limits<type>::ma
 
int n, m;

 
int solve() {
    if (!(cin >> n >> m))
        return 1;
    int ans = min(n, m) * 2;
    n -= ans / 2;
    m -= ans / 2;
    if ((n & 1) || (m & 1))
        ans++;
    cout << ans << '\n';
    return 0;
}
 
signed main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int tests = 1e9;
//    cin >> tests;
    for (int i = 1; i <= tests; i++) {
        if (solve())
            break;
#ifdef LOCAL
        cout << "------------------------------\n";
#endif
    }
}
